
-- ULTIMATE DATABASE INTEGRITY REPAIR
-- This script ensures all tables have the columns required by the sync engine.
-- Run this in the Supabase SQL Editor.

-- 1. INGREDIENTS
ALTER TABLE public.ingredients 
ADD COLUMN IF NOT EXISTS unit TEXT,
ADD COLUMN IF NOT EXISTS category TEXT,
ADD COLUMN IF NOT EXISTS stock_level NUMERIC DEFAULT 0,
ADD COLUMN IF NOT EXISTS current_cost_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS image_url TEXT,
ADD COLUMN IF NOT EXISTS reorder_point NUMERIC DEFAULT 0,
ADD COLUMN IF NOT EXISTS shelf_life_days INT,
ADD COLUMN IF NOT EXISTS preferred_supplier_id UUID,
ADD COLUMN IF NOT EXISTS last_pack_count NUMERIC,
ADD COLUMN IF NOT EXISTS last_pack_size NUMERIC,
ADD COLUMN IF NOT EXISTS last_pack_type TEXT;

-- 2. REQUISITIONS
ALTER TABLE public.requisitions 
ADD COLUMN IF NOT EXISTS requestor_name TEXT,
ADD COLUMN IF NOT EXISTS source_account_id UUID,
ADD COLUMN IF NOT EXISTS unit TEXT,
ADD COLUMN IF NOT EXISTS pack_count NUMERIC,
ADD COLUMN IF NOT EXISTS pack_size NUMERIC,
ADD COLUMN IF NOT EXISTS pack_type TEXT;

-- 3. INVOICES
ALTER TABLE public.invoices 
ADD COLUMN IF NOT EXISTS subtotal_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS service_charge_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS vat_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS manual_set_price_cents BIGINT,
ADD COLUMN IF NOT EXISTS discount_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS standard_total_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS lines JSONB DEFAULT '[]'::jsonb;

-- 4. REUSABLE ITEMS
ALTER TABLE public.reusable_items 
ADD COLUMN IF NOT EXISTS description TEXT,
ADD COLUMN IF NOT EXISTS price_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS stock_quantity INT DEFAULT 0,
ADD COLUMN IF NOT EXISTS stock_level NUMERIC DEFAULT 0,
ADD COLUMN IF NOT EXISTS category TEXT,
ADD COLUMN IF NOT EXISTS category_id UUID,
ADD COLUMN IF NOT EXISTS unit_id UUID,
ADD COLUMN IF NOT EXISTS image TEXT,
ADD COLUMN IF NOT EXISTS image_url TEXT;

-- 5. RENTAL ITEMS
ALTER TABLE public.rental_items 
ADD COLUMN IF NOT EXISTS replacement_cost_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS supplier_id UUID,
ADD COLUMN IF NOT EXISTS category_id UUID,
ADD COLUMN IF NOT EXISTS unit_id UUID,
ADD COLUMN IF NOT EXISTS image_url TEXT;

-- 6. ASSETS
ALTER TABLE public.assets 
ADD COLUMN IF NOT EXISTS asset_class TEXT,
ADD COLUMN IF NOT EXISTS acquisition_cost_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS acquisition_date DATE,
ADD COLUMN IF NOT EXISTS residual_value_cents BIGINT DEFAULT 0,
ADD COLUMN IF NOT EXISTS serial_no TEXT,
ADD COLUMN IF NOT EXISTS location_id UUID,
ADD COLUMN IF NOT EXISTS category_id UUID,
ADD COLUMN IF NOT EXISTS image_url TEXT,
ADD COLUMN IF NOT EXISTS normalized_name TEXT;

-- 7. CATERING EVENTS (ensure financials JSONB exists)
ALTER TABLE public.catering_events 
ADD COLUMN IF NOT EXISTS financials JSONB DEFAULT '{}'::jsonb,
ADD COLUMN IF NOT EXISTS cuisine_details JSONB;

-- RELOAD PostgREST
NOTIFY pgrst, 'reload config';
